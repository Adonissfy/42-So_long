#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*str;

	str = (t_list *)malloc(sizeof(*str));
	if (!str)
		return (NULL);
	str->content = content;
	str->next = NULL;
	return (str);
}
